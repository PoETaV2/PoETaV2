class HateSpeechPT_Binary(PromptSelectionTask):
    VERSION = 0
    DATASET_URL="https://raw.githubusercontent.com/paulafortuna/Portuguese-Hate-Speech-Dataset/refs/heads/master/2019-05-28_portuguese_hate_speech_binary_classification.csv"
    DATASET_LOCAL_PATH = "Portuguese-Hate-Speech-Dataset/data.csv"
    DATASET_NAME = None

    manual_examples = _manual_examples

    KEYS_TO_INDEX = ['text']
    KEY_TO_BALANCE = 'hatespeech_comb'
    NUM_CLASSES = 2
    SEARCHER_K = 600

    def download(self, data_dir=None, cache_dir=None, download_mode=None):
        """ Dowloading the dataset requires the use of an API key. To use the 
        dataset for academic purpose, please contact the corresponding author 
        Henrico Brum. The dataset is originally in TSV format, and comes with 
        a parsing code that converts to a JSON file.
        """
        
        if not os.path.exists(self.DATASET_LOCAL_PATH):
            
            # download the dataset
            request=requests.get(self.DATASET_URL)
            os.makedirs(os.path.dirname(self.DATASET_LOCAL_PATH), exist_ok=True)
            with open(self.DATASET_LOCAL_PATH, "w") as f:
                f.write(request.text)   

        self.dataset_pd = pd.read_csv(self.DATASET_LOCAL_PATH)
        
        self.dataset=collections.defaultdict(list)

        for index,row in self.dataset_pd.iterrows():
            self.dataset["train"].append(self._process_doc(row))

    

    def _process_doc(self, doc):
        # doc is a pandas row 
        #"text","hatespeech_comb","hatespeech_G1","annotator_G1","hatespeech_G2","annotator_G2","hatespeech_G3","annotator_G3"
        target=doc["hatespeech_comb"]
        text=doc["text"]
        
        class_mapping={
            "1":"ofensiva",
            "0":"inofensiva"
        }

        return {
            "text":text,
            "hatespeech_comb":int(target),
            "target_text":class_mapping[str(target)]
        }

    def has_training_docs(self):
        return True

    def has_validation_docs(self):
        return False

    def has_test_docs(self):
        return True

    def training_docs(self):
        return self.dataset["train"]

    def test_docs(self):
        return self.dataset["train"]

    def doc_to_text(self, doc):
        return f"Mensagem: \"{doc['text']}\".\nResposta:"

    def doc_to_target(self, doc):
        answer=doc['target_text']
        return " " + answer

    def construct_requests(self, doc, ctx):
        """ Uses RequestFactory to construct Requests and returns an iterable of 
        Requests which will be sent to the LM.

        :param doc:
            The document as returned from training_docs, validation_docs, or test_docs.
        :param ctx: str
            The context string, generated by fewshot_context. This includes the natural 
            language description, as well as the few shot examples, and the question
            part of the document for `doc`. 
        """
        ll_negativa, _ = rf.loglikelihood(ctx, " inofensiva") 
        ll_positiva, _ = rf.loglikelihood(ctx, " ofensiva") 
        return ll_negativa, ll_positiva
    
    def process_results(self, doc, results):
        """Take a single document and the LM results and evaluates, returning a 
        dict where keys are the names of submetrics and values are the values of 
        the metric for that one document

        :param doc:
            The document as returned from training_docs, validation_docs, or test_docs.
        :param results:
            The results of the requests created in construct_requests.
        """
        gold = 0 if doc['hatespeech_comb'] == "0" else 1
        pred = np.argmax(results)

        return {
            "acc": (pred == gold),
            "f1-macro": (pred, gold),
            "f1-weighted": (pred, gold),
        }

    @classmethod
    def macro_f1(cls, items):
        preds, golds = zip(*items)
        preds = np.array(preds)
        golds = np.array(golds)
        label_set = set(golds)
        macro_f1 = f1_score(golds, preds, average='macro', 
                        labels=list(label_set))
        return macro_f1 * 100.0

    @classmethod
    def weighted_f1(cls, items):
        preds, golds = zip(*items)
        preds = np.array(preds)
        golds = np.array(golds)
        label_set = set(golds)
        weighted_f1 = f1_score(golds, preds, average='weighted', 
                        labels=list(label_set))
        return weighted_f1 * 100.0

    def aggregation(self):
        """
        :returns: {str: [float] -> float}
            A dictionary where keys are the names of submetrics and values are 
            functions that aggregate a list of metrics
        """
        return {
            "acc": mean,
            "f1-macro": self.macro_f1,
            "f1-weighted": self.weighted_f1,
        }

    def higher_is_better(self):
        """
        :returns: {str: bool}
            A dictionary where keys are the names of submetrics and values are 
            whether a higher value of the submetric is better
        """
        return {
            "acc": True,
            "f1-macro": True,
            "f1-weighted": True,
        }


class HateSpeechPT_Greedy(HateSpeechPT_Binary):
    def construct_requests(self, doc, ctx):
        """Uses RequestFactory to construct Requests and returns an iterable of
        Requests which will be sent to the LM.
        :param doc:
            The document as returned from training_docs, validation_docs, or test_docs.
        :param ctx: str
            The context string, generated by fewshot_context. This includes the natural
            language description, as well as the few shot examples, and the question
            part of the document for `doc`.
        """
        continuation = rf.greedy_until(ctx, ['\n'])
        return continuation

    def process_results(self, doc, results):
        """Take a single document and the LM results and evaluates, returning a
        dict where keys are the names of submetrics and values are the values of
        the metric for that one document
        :param doc:
            The document as returned from training_docs, validation_docs, or test_docs.
        :param results:
            The results of the requests created in construct_requests.
        """
        gold = self.doc_to_target(doc).strip().lower()
        pred = results[0].strip().lower()
        
        regex_str='(inofensiva|ofensiva)'
        # get first match
        match = re.search(regex_str, pred)
        if match:
            regex_pred = match.group(1)
        else:
            regex_pred = pred
            
            
        if pred==gold:
            acc=1
        elif regex_pred==gold:
            acc=1
        else:
            acc=0
        
        debug_info={
            "gold":gold,
            "pred":pred,
            "regex_pred":regex_pred
        }
        
        return {
            "acc": acc,
            "f1-macro": (pred, gold),
            "f1-weighted": (pred, gold),
            "debug_info":debug_info
        }
    